using System;

namespace SomerenModel
{
    public class Student
    {
        public int StudentNumber { get; set; }     // Student Number, e.g. 713262
        public string RoomNumber { get; set; } // Room number, e.g. A102
        public string FirstName { get; set; } // First Name, e.g. Tina
        public string LastName { get; set; } // Last Name, e.g. Naseri
        public int Telephone { get; set; } // Telephone number, e.g. 617652301
        public string Class { get; set; } // Student's class, e.g. IT1D
    }
}